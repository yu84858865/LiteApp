public class DataCollegeBean {
    private String time;
    private String info;
    private String size;
    private String url;

    public String getTime() {
        return time;
    }

    public DataCollegeBean setTime(String time) {
        this.time = time;
        return this;
    }

    public String getInfo() {
        return info;
    }

    public DataCollegeBean setInfo(String info) {
        this.info = info;
        return this;
    }

    public String getSize() {
        return size;
    }

    public DataCollegeBean setSize(String size) {
        this.size = size;
        return this;
    }

    public String getUrl() {
        return url;
    }

    public DataCollegeBean setUrl(String url) {
        this.url = url;
        return this;
    }
}
